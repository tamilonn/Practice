package code.test.tamil;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		//Assignment operators are =, +=, -=, *=, /=
		
		//+=
		int num1 = 10;
		System.out.println("num1 now is " + num1);
		num1 += 10;
		System.out.println("num1 now is " + num1);
		
		//-=
		int num2 = 25;
		System.out.println("num2 now is " + num2);
		num2 -= 8;
		System.out.println("num2 now is " + num2);
		
		//*=
		int num3 = 6;
		System.out.println("num3 now is " + num3);
		num3 *= 7;
		System.out.println("num3 now is " + num3);
		
		//(/)=
		int num4 = 10;
		System.out.println("num4 now is " + num4);
		num4 /= 2;
		System.out.println("num4 now is " + num4);

	}

}
