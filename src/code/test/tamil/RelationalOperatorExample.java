package code.test.tamil;

public class RelationalOperatorExample {

	public static void main(String[] args) {
		
		 	int num1 =10;
		    int num2 =20;
		     
		     
		    System.out.println("num1 > num2 is " + (num1 > num2));
		    System.out.println("num1 < num2 is " + (num1 < num2));
		    System.out.println("num1 >= num2 is " + (num1 >= num2));
		    System.out.println("num1 <= num2 is " + (num1 <= num2));
		    System.out.println("num1 == num2 is " + (num1 == num2));
		    System.out.println("num1 != num2 is " + (num1 != num2));
	}

}
